/* ── Polish mode ───────────────────────────────────────────────────────────── */

const POLISH_PROMPT_LABELS: Record<keyof PolishResult["prompts"], string> = {
  codex: "Codex",
  claude: "Claude",
  chatgpt: "ChatGPT",
  cursor: "Cursor",
  generic: "Any AI agent",
};

export function exportPolishMarkdown(result: PolishResult): string {
  const date = new Date().toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric" });
  const lines: string[] = [result.designMd.trim(), ``, `## Design Tokens`, ``];

  lines.push(`### Tailwind`, ``, "```css", result.tokens.tailwind.trim(), "```", ``);
  lines.push(`### CSS Variables`, ``, "```css", result.tokens.css.trim(), "```", ``);
  lines.push(`### JSON`, ``, "```json", result.tokens.json.trim(), "```", ``);

  if (result.critiqueTrail?.length) {
    lines.push(`## Quality passes`, ``);
    result.critiqueTrail.forEach((c, i) => {
      lines.push(`- **Pass ${i + 1}: ${c.score}/100** — ${c.verdict}`);
    });
    lines.push(``);
  }

  if (result.screens.length) {
    lines.push(`## Rendered Screens`, ``);
    for (const s of result.screens) {
      lines.push(`### ${s.name}`, ``, "```html", s.html.trim(), "```", ``);
    }
  }

  lines.push(`## Apply-this-design Prompts`, ``);
  for (const key of Object.keys(result.prompts) as (keyof PolishResult["prompts"])[]) {
    lines.push(`### ${POLISH_PROMPT_LABELS[key]}`, ``, "```text", result.prompts[key].trim(), "```", ``);
  }

  lines.push(`*Generated: ${date}*`, `---`, `*Generated by [PlainCode](https://plaincode-production.up.railway.app)*`);

  return lines.join("\n");
}
